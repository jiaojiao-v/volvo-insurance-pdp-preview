/**
 * Format a price amount as a localised currency string.
 * @param {number} amount - The price amount to format
 * @param {string} currency - ISO 4217 currency code (e.g. 'SEK', 'USD')
 * @param {string} locale - BCP 47 locale string (e.g. 'sv-SE', 'en-GB')
 * @returns {string} Formatted price string (e.g. '£45', '249 kr')
 */
export function formatPrice(amount, currency, locale) {
  return new Intl.NumberFormat(locale, {
    style: 'currency',
    currency,
    maximumFractionDigits: 0,
  }).format(amount)
}
